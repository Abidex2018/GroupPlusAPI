                if (searchObj.Status > -1)
                    thisDisciplines = thisDisciplines.FindAll(p => p.Status == (ItemStatus) searchObj.Status);

                var disciplineItems = new List<DisciplineObj>();
                thisDisciplines.ForEachx(m =>
                {
                    disciplineItems.Add(new DisciplineObj
                    {
                        DisciplineId = m.DisciplineId,
                        Name = m.Name,
                        Status = (int) m.Status,
                        StatusLabel = m.Status.ToString().Replace("_", " ")
