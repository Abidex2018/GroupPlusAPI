                {
                    departmentItems.Add(new DepartmentObj
                    {
                        DepartmentId = m.DepartmentId,
                        Name = m.Name,
                        Status = (int) m.Status,
                        StatusLabel = m.Status.ToString().Replace("_", " ")
                    });
                });

